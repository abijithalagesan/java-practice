class Functions {

    static void add(int a, int b){
        System.out.println("Sum = " + (a + b));
    }

    static void multiply(int a, int b){
        System.out.println("Product = " + (a * b));
    }

    public static void main(String[] args) {

        add(10, 5);
        multiply(4, 3);

    }
}
