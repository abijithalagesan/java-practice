class Loops {
    public static void main(String[] args) {

        // for loop
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }

        // while loop
        int j = 1;
        while(j <= 3){
            System.out.println("While: " + j);
            j++;
        }

        // do-while loop
        int k = 1;
        do{
            System.out.println("Do While: " + k);
            k++;
        }while(k <= 2);

    }
}
